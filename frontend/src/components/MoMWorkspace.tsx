/* ── Types ─────────────────────────────────────────────────────────────── */

export type Priority = "high" | "medium" | "low"

export interface ActionItem {
  task: string
  owner: string
  due_date: string
  priority: Priority
}

export interface KeyDecision {
  decision: string
  rationale: string
  stakeholders: string[]
}

export interface MeetingMoM {
  title: string
  date_context: string
  participants: string[]
  executive_summary: string
  key_decisions: KeyDecision[]
  action_items: ActionItem[]
  topics_discussed: string[]
  next_steps: string[]
}

interface ApiSuccess {
  success: true
  filename: string
  mom: MeetingMoM
}

type Phase = "idle" | "processing" | "done" | "error"

const ACCEPTED =
  ".mp4,.mkv,.webm,.mov,.avi,.mp3,.wav,.m4a,.aac,.ogg,.flac,audio/*,video/*"

const LOADING_COPY = [
  "Uploading media securely…",
  "Extracting audio track…",
  "Feeding audio to Gemini…",
  "Listening for key decisions…",
  "Identifying action items…",
  "Synthesizing meeting insights…",
  "Formatting your minutes…",
  "Almost there — polishing the summary…",
]

const PRIORITY_STYLES: Record<Priority, string> = {
  high: "bg-red-500/15 text-red-400 border-red-500/30",
  medium: "bg-amber-500/15 text-amber-400 border-amber-500/30",
  low: "bg-emerald-500/15 text-emerald-400 border-emerald-500/30",
}

/* ── Helpers ───────────────────────────────────────────────────────────── */

function isVideoFile(file: File): boolean {
  if (file.type.startsWith("video/")) return true
  return /\.(mp4|mkv|webm|mov|avi)$/i.test(file.name)
}

function formatBytes(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`
}

function momToMarkdown(mom: MeetingMoM): string {
  const lines: string[] = []
  lines.push(`# ${mom.title}`)
  lines.push("")
  if (mom.date_context) lines.push(`**Date / context:** ${mom.date_context}`)
  if (mom.participants?.length) {
    lines.push(`**Participants:** ${mom.participants.join(", ")}`)
  }
  lines.push("")
  lines.push("## Executive Summary")
  lines.push("")
  lines.push(mom.executive_summary)
  lines.push("")

  if (mom.topics_discussed?.length) {
    lines.push("## Topics Discussed")
    lines.push("")
    for (const t of mom.topics_discussed) lines.push(`- ${t}`)
    lines.push("")
  }

  lines.push("## Key Decisions")
  lines.push("")
  if (!mom.key_decisions?.length) {
    lines.push("_No key decisions recorded._")
  } else {
    for (const d of mom.key_decisions) {
      lines.push(`### ${d.decision}`)
      lines.push("")
      lines.push(`**Rationale:** ${d.rationale}`)
      if (d.stakeholders?.length) {
        lines.push(`**Stakeholders:** ${d.stakeholders.join(", ")}`)
      }
      lines.push("")
    }
  }

  lines.push("## Action Items")
  lines.push("")
  if (!mom.action_items?.length) {
    lines.push("_No action items recorded._")
  } else {
    for (const a of mom.action_items) {
      lines.push(
        `- [ ] **${a.task}** — _${a.owner}_ · due ${a.due_date} · priority: ${a.priority}`
      )
    }
  }
  lines.push("")

  if (mom.next_steps?.length) {
    lines.push("## Next Steps")
    lines.push("")
    for (const s of mom.next_steps) lines.push(`- ${s}`)
    lines.push("")
  }

  lines.push("---")
  lines.push("_Generated by Meetingly · not stored on any server_")
  return lines.join("\n")
}

